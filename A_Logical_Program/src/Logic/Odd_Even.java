package Logic;
//print 1 to 100 even or odd

public class Odd_Even {
	public static void main(String[] args) {
		for(int i=1;i<100;i++)
		{
			if(i%2==0)
				System.out.println("Even number:"+i);
		}
			for(int j=0;j<100;j++)
			{
				if(j%2!=0)
					System.out.println("Odd number:"+j);
			}
		
	}
}
